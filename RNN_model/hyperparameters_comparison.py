def avg(arr,n):
    l = 0
    r = l+n-1
    
    arr2 = np.copy(arr)

    while r <= len(arr2)-1:
        avg = sum(arr2[l:r+1])/n
        
        for i in range(l,r+1):
            arr2[i] = avg
        # update:
        l = l + n
        r = r+ n
    
    return arr2

